export type GlobalTranslationKey = keyof typeof g.en;

export function gt(lang: GlobalLanguage, key: GlobalTranslationKey): string {
  return g[lang]?.[key] ?? g.en[key] ?? key;
}

export function getStoredGlobalLanguage(): GlobalLanguage {
  if (typeof window === "undefined") return "en";
  const stored = localStorage.getItem("arya_global_lang");
  if (stored && ["en", "es", "ru", "fr", "tr"].includes(stored)) {
    return stored as GlobalLanguage;
  }
  return "en";
}

export function setStoredGlobalLanguage(lang: GlobalLanguage) {
  localStorage.setItem("arya_global_lang", lang);
}
