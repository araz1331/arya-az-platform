export type TranslationKey = keyof typeof translations.az;

export function t(lang: Language, key: TranslationKey): string {
  return translations[lang]?.[key] ?? translations.az[key] ?? key;
}

export function getStoredLanguage(): Language {
  if (typeof window === "undefined") return "az";
  const stored = localStorage.getItem("arya_lang");
  if (stored && ["az", "en", "fr", "es", "ru", "tr"].includes(stored)) {
    return stored as Language;
  }
  return "az";
}

export function setStoredLanguage(lang: Language) {
  localStorage.setItem("arya_lang", lang);
}
